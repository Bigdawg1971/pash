        public string OutputAssembly
        {
            get
            {
                return this.outputAssembly;
            }
            set
            {
                this.outputAssembly = value;
                if (this.outputAssembly != null)
                {
                    this.outputAssembly = this.outputAssembly.Trim();
                    ProviderInfo provider = null;
                    Collection<string> resolvedProviderPathFromPSPath = new Collection<string>();
                    try
                    {
                        resolvedProviderPathFromPSPath = base.Context.SessionState.Path.GetResolvedProviderPathFromPSPath(this.outputAssembly, out provider);
                    }
                    catch (ItemNotFoundException)
                    {
                    }
                    ErrorRecord errorRecord = new ErrorRecord(new Exception(StringUtil.Format(AddTypeStrings.OutputAssemblyDidNotResolve, this.outputAssembly)), "INVALID_OUTPUT_ASSEMBLY", ErrorCategory.InvalidArgument, this.outputAssembly);
                    if (!string.Equals("FileSystem", provider.Name, StringComparison.OrdinalIgnoreCase))
                    {
                        base.ThrowTerminatingError(errorRecord);
                    }
                    else if (resolvedProviderPathFromPSPath.Count > 1)
                    {
                        base.ThrowTerminatingError(errorRecord);
                    }
                    else if (resolvedProviderPathFromPSPath.Count == 0)
                    {
                        if (WildcardPattern.ContainsWildcardCharacters(this.outputAssembly))
                        {
                            base.ThrowTerminatingError(errorRecord);
                        }
                        else
                        {
                            this.outputAssembly = base.Context.SessionState.Path.GetUnresolvedProviderPathFromPSPath(this.outputAssembly);
                        }
                    }
                    else
                    {
                        this.outputAssembly = resolvedProviderPathFromPSPath[0];
                    }
                }
            }
        }

